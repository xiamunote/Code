	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		final Membership other = (Membership) obj;
		if (membId == null) {
			if (other.membId != null)
				return false;
		} else if (!membId.equals(other.membId))
			return false;
		return true;
	}

